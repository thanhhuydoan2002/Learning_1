void conditionalExpression(int n) {
  //IF ELSE
  if (n == 1)
    print('so mot');
  else if (n == 99)
    print('so may man');
  else
    print('so khac');

  //SWITCH CASE
  switch (n) {
    case 1:
      print('number one');
      break;
    case 99:
      print('lucky number');
      break;
    default:
      print('other number');
      break;
  }

  print('\n');
}
